#include "push_swap.h"

bool	stack_sorted(t_stack *stack)
{
	if (!stack)
		return (1);
	while (stack->next)
	{
		if(stack->number > stack->next->number)
			return (false);
		stack = stack->next;
	}
	return (true);
}